CIRCLED = "①②③④⑤⑥⑦⑧⑨⑩⑪⑫⑬⑭⑮⑯⑰⑱⑲⑳"

def hang_key_to_number(key: str) -> str:
    """'hang#①' → '①', 'hang#1' → '1' """
    return key.replace("hang#", "")

def match_hang(hang_list: list, number_str: str) -> dict | None:
    """hang number (①, 1, etc.) 로 hang 찾기"""
    for h in hang_list:
        hn = str(h.get("number", ""))
        if hn == number_str:
            return h
        # ① vs "1" 매칭
        if number_str in CIRCLED:
            idx = CIRCLED.index(number_str)
            if hn == str(idx + 1):
                return h
        elif number_str.isdigit():
            idx = int(number_str) - 1
            if 0 <= idx < len(CIRCLED) and hn == CIRCLED[idx]:
                return h
    return None


