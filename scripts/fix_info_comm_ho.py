def main():
    data = json.loads(DATA_FILE.read_text())
    changes = 0

    for art in data:
        jo = art["jo"]
        if jo not in DATA:
            continue

        for loc_key, ho_items in DATA[jo].items():
            if loc_key == "content":
                if not art.get("ho"):
                    art["ho"] = ho_items
                    changes += 1
                    print(f"  {jo} content: ho {len(ho_items)}건 추가")
            elif loc_key.startswith("hang#"):
                num = loc_key.replace("hang#", "")
                target = None
                for h in art.get("hang", []):
                    hn = str(h.get("number", ""))
                    if hn == num:
                        target = h
                        break
                    if num in CIRCLED:
                        idx = CIRCLED.index(num)
                        if hn == str(idx + 1):
                            target = h
                            break
                    elif num.isdigit():
                        idx = int(num) - 1
                        if 0 <= idx < len(CIRCLED) and hn == CIRCLED[idx]:
                            target = h
                            break
                if target and not target.get("ho"):
                    target["ho"] = ho_items
                    changes += 1
                    print(f"  {jo} {loc_key}: ho {len(ho_items)}건 추가")
                elif not target:
                    print(f"  경고: {jo} {loc_key} hang 없음")

    print(f"\n총 {changes}건 ho[] 추가")
    DATA_FILE.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n")
    print(f"저장: {DATA_FILE}")


if __name__ == "__main__":
    main()
